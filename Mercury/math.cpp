#include "./include/math.h"

float fcos(float x) { return cos(x); }
float fsin(float x) { return sin(x); }
float ftan(float x) { return tan(x); }
float fcot(float x) { return 1 / tan(x); }
float fexp(float x) { return exp(x); }
float flog(float x) { return log(x); }
float fsqrt(float x) { return sqrt(x); }